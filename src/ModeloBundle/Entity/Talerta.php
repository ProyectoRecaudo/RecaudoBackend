<?php

namespace ModeloBundle\Entity;

/**
 * Talerta
 */
class Talerta
{
    /**
     * @var string
     */
    private $tipoalerta;

    /**
     * @var string
     */
    private $reglaalerta;

    /**
     * @var \DateTime
     */
    private $creacionalerta = 'now()';

    /**
     * @var \DateTime
     */
    private $modificacionalerta = 'now()';

    /**
     * @var integer
     */
    private $pkidalerta;


    /**
     * Set tipoalerta
     *
     * @param string $tipoalerta
     *
     * @return Talerta
     */
    public function setTipoalerta($tipoalerta)
    {
        $this->tipoalerta = $tipoalerta;

        return $this;
    }

    /**
     * Get tipoalerta
     *
     * @return string
     */
    public function getTipoalerta()
    {
        return $this->tipoalerta;
    }

    /**
     * Set reglaalerta
     *
     * @param string $reglaalerta
     *
     * @return Talerta
     */
    public function setReglaalerta($reglaalerta)
    {
        $this->reglaalerta = $reglaalerta;

        return $this;
    }

    /**
     * Get reglaalerta
     *
     * @return string
     */
    public function getReglaalerta()
    {
        return $this->reglaalerta;
    }

    /**
     * Set creacionalerta
     *
     * @param \DateTime $creacionalerta
     *
     * @return Talerta
     */
    public function setCreacionalerta($creacionalerta)
    {
        $this->creacionalerta = $creacionalerta;

        return $this;
    }

    /**
     * Get creacionalerta
     *
     * @return \DateTime
     */
    public function getCreacionalerta()
    {
        return $this->creacionalerta;
    }

    /**
     * Set modificacionalerta
     *
     * @param \DateTime $modificacionalerta
     *
     * @return Talerta
     */
    public function setModificacionalerta($modificacionalerta)
    {
        $this->modificacionalerta = $modificacionalerta;

        return $this;
    }

    /**
     * Get modificacionalerta
     *
     * @return \DateTime
     */
    public function getModificacionalerta()
    {
        return $this->modificacionalerta;
    }

    /**
     * Get pkidalerta
     *
     * @return integer
     */
    public function getPkidalerta()
    {
        return $this->pkidalerta;
    }
}
